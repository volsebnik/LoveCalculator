public class Par {
	
	String ime1;
	String ime2;
	int kompatibilnost;
	
	public String getIme1() {
		return ime1;
	}
	public void setIme1(String ime1) {
		this.ime1 = ime1;
	}
	public String getIme2() {
		return ime2;
	}
	public void setIme2(String ime2) {
		this.ime2 = ime2;
	}
	public int getKompatibilnost() {
		return kompatibilnost;
	}
	public void setKompatibilnost(int kompatibilnost) {
		this.kompatibilnost = kompatibilnost;
	}
	public Par(String ime1, String ime2, int kompatibilnost) {
		super();
		this.ime1 = ime1;
		this.ime2 = ime2;
		this.kompatibilnost = kompatibilnost;
	}
	
	
	
	
	

}
